def add(a, b):
    """
    Returns the sum of a and b.

    Args:
        a (float or int): First number.
        b (float or int): Second number.

    Returns:
        float or int: The sum of a and b.
    """
    return a + b

def sub(a, b):
    """
    Returns the difference of a and b.

    Args:
        a (float or int): First number.
        b (float or int): Second number.
    Returns:
        float or int: The difference of a and b.    
    """
    return a - b